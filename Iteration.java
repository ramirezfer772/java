// Repetir

public class Iteration {
  public static void main(String[] args) {
	  
	  for (int i = 0; i <= 5; i++) {
		 System.out.println(i);
		  
	  }
	  
	  
	  System.out.println("--------");
	  
	  
	  for (int j = 0; j <= 5; j++) {
		  int multiply = 2 * j;
		  System.out.println(multiply);
	  }

	}
}

